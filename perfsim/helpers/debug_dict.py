from __future__ import annotations

from typing import Union, TypedDict


class DebugDict(TypedDict):
    """
    This class represents a dictionary of debug parameters.
    """

    debug: bool
    debug_level: int
    debug_file_location: Union[None, str]
    log_cpu_events: bool
    log_timeline: bool
